void Set_Value(uint16_t red, uint16_t green, uint16_t blue)
{
	__HAL_TIM_SetCompare(&htim2, TIM_CHANNEL_2, red);			//SET value for CH2
	__HAL_TIM_SetCompare(&htim2, TIM_CHANNEL_3, green);		//SET value for CH3
	__HAL_TIM_SetCompare(&htim2, TIM_CHANNEL_4, blue);		//SET value for CH4
}

void Set_Color(void)
{
	Set_Value(0, 0, 0);    // off Led RGB 500ms
	HAL_Delay(500);
	
	Set_Value(254, 0, 0);		// On Red_RGB
	HAL_Delay(500);  
	
	Set_Value(0, 254, 0);		// On Green_RGB
	HAL_Delay(500);  
	
	Set_Value(0, 0, 254);		// On Blue_RGB
	HAL_Delay(500); 
	
	/*Rainbow Function*/
	for(int color = 0; color <= 763; color++)
	{
		if(color <=254)
		{
			Set_Value(254-color, color, 0) ;
		}
		else if(color <= 508)
		{
			Set_Value(0, 254 - (color-254), color - 254);
		}
		else
		{
			Set_Value(color - 509, 0, 254 - (color -509) );
		}
		HAL_Delay(10);
	}
	
	
}
