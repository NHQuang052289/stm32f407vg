void PWM_TIM4(void)
{
	for(int duty = 0; duty <= 99; duty++)
	{	
	__HAL_TIM_SetCompare(&htim4, TIM_CHANNEL_1, duty);
	__HAL_TIM_SetCompare(&htim4, TIM_CHANNEL_2, duty);
	__HAL_TIM_SetCompare(&htim4, TIM_CHANNEL_3, duty);
	__HAL_TIM_SetCompare(&htim4, TIM_CHANNEL_4, duty);
		HAL_Delay(30);
	}
	HAL_Delay(500);
	
	for(int duty = 99; duty >= 0; duty++)
	{	
	__HAL_TIM_SetCompare(&htim4, TIM_CHANNEL_1, duty);
	__HAL_TIM_SetCompare(&htim4, TIM_CHANNEL_2, duty);
	__HAL_TIM_SetCompare(&htim4, TIM_CHANNEL_3, duty);
	__HAL_TIM_SetCompare(&htim4, TIM_CHANNEL_4, duty);
		HAL_Delay(30);
	}
	HAL_Delay(500);
}
