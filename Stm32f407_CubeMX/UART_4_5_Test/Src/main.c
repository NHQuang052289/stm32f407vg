/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
	
  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_UART4_Init();
  MX_UART5_Init();
	
	HAL_UART_Receive_IT(&huart4, &receive_data, 1);
	HAL_UART_Receive_IT(&huart5, &echo_receive_data, 1);

  while (1)
  {
		HAL_UART_Transmit_IT(&huart4, &send_data, 1);			//UART4 send data to UART5
		send_data++;
		HAL_Delay(1000);
  }
  /* USER CODE END 3 */
}

void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	
	if(huart->Instance == huart5.Instance)
	{
		HAL_UART_Receive_IT(&huart5, &receive_data, 1);				//UART5 receive data, save to receive_data variable
		HAL_UART_Transmit_IT(&huart5, &receive_data, 1);			//UART5 send data to UART 4
	}		
	if(huart->Instance == huart4.Instance)
	{
		HAL_UART_Receive_IT(&huart4, &echo_receive_data, 1);	//UART5 receive data, save to echo_receive_data variable
	}
}
